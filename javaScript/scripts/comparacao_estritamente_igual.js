/*
Operadores de comparação estritamente igual e estritamente diferente
*/

// 



let one = 1 
let two = 2
// === estritamente iguál a
console.log(two === 1)
console.log(one === '1')

// !== estritamente diferente de 
console.log(one !== two)
console.log(one !== 1)
console.log(one !== "1")










