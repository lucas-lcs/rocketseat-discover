/*
Para transformar uma string em letras maiúsculas pode-se usar o método toUpperCase(), 
e ao contrário, também é possível usar o toLowerCase() para transformar uma string em todas letras minúsculas.

*/

// MANIPULANDO STRINGS E NUMEROS

// Transformar letras minúsculas em maiúsculas. Faça o contrário disso também

let word = 'Programar é muito bacana!'
console.log(word.toLowerCase()) //programar é muito bacana!
console.log(word.toLocaleUpperCase())// PROGRAMAR É MUITO BACANA!






